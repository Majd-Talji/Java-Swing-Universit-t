    private void marks() {

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        try {
//          //            id_student + id_course + name_course + hour + name_doctor + mark1 + mark2
            con = ConnectionDataBase.getConnection();
            com = con.prepareStatement("SELECT * FROM register_student_course WHERE id_student = '" + Integer.parseInt(Login.id) + "'");
            rst = com.executeQuery();

            while (rst.next()) {

                float mark1 = rst.getFloat("mark1");
                float mark2 = rst.getFloat("mark2");
                float total = mark1 + mark2;
                String gr = tagder(total);

                model.addRow(new Object[]{rst.getInt("id_course"), rst.getString("name_course"), rst.getFloat("hour"), mark1, mark2, total, gr});

                float hour = rst.getFloat("hour");
                countHour += hour;
                totals += (total * hour);

            }

            float avg = totals / countHour;
            avgLabel.setText(String.valueOf(avg));
            greateLabel.setText(tagder(avg));

        } catch (Exception e) {
        } finally {
            try {
                con.close();
                rst.close();
            } catch (Exception ex) {
            }
        }

    }

    public static String tagder(float total) {

        if (total >= 90) {
            return "Super";
        } else if (total >= 80) {
            return "Sehr Gut";
        } else if (total >= 70) {
            return "Gut";
        } else if (total >= 60) {
            return "Zul채ssig";
        } else {
            return "Durchgefallen";
        }

    }

