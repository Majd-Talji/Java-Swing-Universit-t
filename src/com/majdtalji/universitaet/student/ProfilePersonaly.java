    private void information(int id) {
        //id_student+name_student+password+mobile+gender+avg+required_amount+amount_paid+rest_of_the_fees
        try {
            con = ConnectionDataBase.getConnection();
            com = con.prepareStatement("SELECT * FROM student WHERE id_student = '" + id + "'");
            rst = com.executeQuery();

            rst.next();
            //id_student + name_student + password + mobile + gender + avg + required_amount + amount_paid + rest_of_the_fees

            this.id.setText(rst.getString("id_student"));
            name.setText(rst.getString("name_student"));
            mobile.setText(rst.getString("mobile"));
            geschlech.setText(rst.getString("gender")); 
            avg.setText(rst.getString("avg")); 
            requiredAmount.setText(rst.getString("required_amount") + " Euro"); 
            amountPaid.setText(rst.getString("amount_paid") + " Euro"); 
            fees.setText(rst.getString("rest_of_the_fees") + " Euro");  
            
        } catch (Exception e) {
        } finally {
            try {
                con.close();
                rst.close();
            } catch (Exception ex) {
            }
        }

    }

