    private void confPassBetweenNewPass() {
        String newPassword = String.valueOf(newPass.getText());
        String confPassword = String.valueOf(cPass.getText());

        if (!cPass.getText().equals("") && !newPass.getText().equals("")) {
            if (confPassword.equals(newPassword)) {
                jLabel7.setText("Sind identisch");
                jLabel7.setForeground(Color.GREEN);
//                jLabel7.setVisible(true);
            } else {
                jLabel7.setText("Sind identisch nicht, bitte ändern");
                jLabel7.setForeground(Color.red);
//                jLabel7.setVisible(true);
            }
        }
    }

    private void oldPassBettwenNewPass() {
        String oldPassword = String.valueOf(oldPass.getText());
        String newPassword = String.valueOf(newPass.getText());

        if (!oldPass.getText().equals("") && !newPass.getText().equals("")) {
            if (oldPassword.equals(newPassword)) {
//                jLabel6.setText("Altes Passwort");
                jLabel6.setForeground(Color.red);
                jLabel6.setVisible(true);
                cPass.setRequestFocusEnabled(false);
            } else {
//                jLabel6.setText("Altes Passwort");
//                jLabel6.setForeground(Color.black);
                jLabel6.setForeground(jPanel1.getBackground());
//                jLabel6.setVisible(false);
                cPass.setRequestFocusEnabled(true);
            }
        }
    }

