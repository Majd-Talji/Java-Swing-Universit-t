    private void changePass(String nameTable) {

        String miss = "Sie haben vergessen, die folgenden Daten einzugeben :\n";
        if (oldPass.getText().equals("")) {
            miss += oldPassLabel.getText() + "\n";
        }
        if (newPass.getText().equals("")) {
            miss += newPassLabel.getText() + "\n";
        }
        if (cPass.getText().equals("")) {
            miss += cPassLabel.getText() + "\n";
        }
        if (!oldPass.getText().equals("") && !newPass.getText().equals("") && !cPass.getText().equals("")) {
//            id_student + name_student + password + mobile + gender + avg + required_amount + amount_paid + rest_of_the_fees
            //id_doctor+name_doctor+password+mobile+address+gender
            try {
                con = ConnectionDataBase.getConnection();
                com = con.prepareStatement("SELECT password FROM " + nameTable + " WHERE id_" + nameTable + " = '" + Integer.parseInt(id.getText()) + "'");
                rst = com.executeQuery();

                rst.next();
                if (oldPass.getText().equals(rst.getString("password"))) {
                    com = con.prepareStatement("UPDATE " + nameTable + " SET password='" + newPass.getText() + "' WHERE id_" + nameTable + "='" + Integer.parseInt(id.getText()) + "'");
                    com.execute();

                    JOptionPane.showMessageDialog(null, "Passwort wurden erfolgreich geändert.", "Modifiziert", JOptionPane.INFORMATION_MESSAGE);
                    oldPass.setText("");
                    newPass.setText("");
                    cPass.setText("");
                    jLabel6.setForeground(jPanel1.getBackground());
                    jLabel7.setForeground(jPanel1.getBackground());

                } else {
                    JOptionPane.showMessageDialog(null, "Das alte Passwort ist falsch", "Fehler", 2);
                }

            } catch (Exception e) {
            } finally {
                try {
                    con.close();
                    rst.close();
                } catch (Exception ex) {
                }
            }

        } else {
            JOptionPane.showMessageDialog(null, miss, "Datenverlust", 2);
        }

    }

