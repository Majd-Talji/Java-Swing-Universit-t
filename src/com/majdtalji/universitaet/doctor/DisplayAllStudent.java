    private void chooseCourseItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_chooseCourseItemStateChanged

        if (trueFalse) {

            PreparedStatement com1 = null;
            ResultSet rst1 = null;

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            try {

//            id_student + id_course + name_course + hour + name_doctor + mark1 + mark2
                con = ConnectionDataBase.getConnection();
                com = con.prepareStatement("SELECT * FROM register_student_course WHERE name_course = '" + chooseCourse.getSelectedItem().toString() + "'");
                rst = com.executeQuery();

                while (rst.next()) {
                    //id_student + name_student + password + mobile + gender + avg + required_amount + amount_paid + rest_of_the_fees
                    com1 = con.prepareStatement("SELECT * FROM student WHERE id_student = '" + rst.getString("id_student") + "'");
                    rst1 = com1.executeQuery();

                    rst1.next();
                    String nameStudent = rst1.getString("name_student");

                    model.addRow(new Object[]{rst.getString("id_student"), nameStudent, rst.getFloat("mark1"), rst.getFloat("mark2")});
                }

                if (model.getRowCount() > 0) {
                    jButton1.setEnabled(true);
                } else {
                    jButton1.setEnabled(false);
                }

            } catch (Exception e) {
            } finally {
                try {
                    con.close();
                    rst.close();
                } catch (Exception ex) {
                }
            }
        }

    }//GEN-LAST:event_chooseCourseItemStateChanged

