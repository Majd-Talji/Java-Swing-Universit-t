    private void adminLogin() {

        user = idOrName.getText().trim();
        String password = pass.getText().trim();
        if (user.equals("admin") && password.equals("123456")) {
            MainMenu mainMenu = new MainMenu();
            mainMenu.d.setEnabled(false);
            mainMenu.s.setEnabled(false);
            mainMenu.setVisible(true);
            this.setVisible(false);
            JOptionPane.showMessageDialog(null, "Willkommen, " + user);
        } else {
            JOptionPane.showMessageDialog(null, "Überprüfen Sie die Gültigkeit der eingegebenen Daten!", "Die Informationen sind falsch", JOptionPane.ERROR_MESSAGE);
        }

    }

    private void doctorLogin() {
        //id_doctor+name_doctor+password+mobile+address+gender
        id = idOrName.getText().trim();
        String password = pass.getText().trim();

        try {
            con = ConnectionDataBase.getConnection();
            com = con.prepareStatement("SELECT * FROM doctor WHERE id_doctor = '" + Integer.parseInt(id) + "' AND password = '" + password + "'");
            rst = com.executeQuery();

            if (rst.next()) {
                user = rst.getString("name_doctor");
                MainMenu mainMenu = new MainMenu();
                mainMenu.a.setEnabled(false);
                mainMenu.s.setEnabled(false);
                mainMenu.setVisible(true);
                this.setVisible(false);
                JOptionPane.showMessageDialog(null, "Willkommen, Dr." + user);
            } else {
                JOptionPane.showMessageDialog(null, "Überprüfen Sie die Gültigkeit der eingegebenen Daten!", "Die Informationen sind falsch", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
        } finally {
            try {
                con.close();
            } catch (Exception ex) {
            }
        }

    }

    private void studentLogin() {
//        	id_student+name_student+password+mobile+gender+avg+required_amount+amount_paid+rest_of_the_fees
        id = idOrName.getText().trim();
        String password = pass.getText().trim();

        try {
            con = ConnectionDataBase.getConnection();
            com = con.prepareStatement("SELECT * FROM student WHERE id_student = '" + Integer.parseInt(id) + "' AND password = '" + password + "'");
            rst = com.executeQuery();

            if (rst.next()) {
                user = rst.getString("name_student");
                MainMenu mainMenu = new MainMenu();
                mainMenu.a.setEnabled(false);
                mainMenu.d.setEnabled(false);
                mainMenu.setVisible(true);
                this.setVisible(false);
                JOptionPane.showMessageDialog(null, "Willkommen, " + user);
            } else {
                JOptionPane.showMessageDialog(null, "Überprüfen Sie die Gültigkeit der eingegebenen Daten!", "Die Informationen sind falsch", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
        } finally {
            try {
                con.close();
                rst.close();
            } catch (Exception ex) {
            }
        }
    }

