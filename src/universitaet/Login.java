    private void loginBActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginBActionPerformed

        String miss = "Sie haben vergessen, die folgenden Daten einzugeben :\n";
        if (idOrName.getText().equals("")) {
            miss += idLabel.getText() + "\n";
        }
        if (pass.getText().equals("")) {
            miss += passLabel.getText() + "\n";
        }
        if (selectPerson.getSelectedIndex() == -1) {
            miss += selectPersonLabel.getText() + "\n";
        }
        if (!idOrName.getText().equals("") && !pass.getText().equals("") && selectPerson.getSelectedIndex() != -1) {

            if (selectPerson.getSelectedIndex() == 0) {
                adminLogin();
            } else if (selectPerson.getSelectedIndex() == 1) {
                if (!isDigit(idOrName.getText())) {
                    JOptionPane.showMessageDialog(null, "Bitte geben Sie nicht die Text-ID ein.", "Datenverlust", 2);
                } else {
                    doctorLogin();
                }
            } else if (selectPerson.getSelectedIndex() == 2) {
                if (!isDigit(idOrName.getText())) {
                    JOptionPane.showMessageDialog(null, "Bitte geben Sie nicht die Text-ID ein.", "Datenverlust", 2);
                } else {
                    studentLogin();
                }
            }

        } else {
            JOptionPane.showMessageDialog(null, miss, "Datenverlust", 2);
        }

    }//GEN-LAST:event_loginBActionPerformed

    public static boolean isDigit(String... text) {
        for (String s : text) {
            if (!s.matches("[0-9]+")) {
                return false;
            }
        }
        return true;
    }

